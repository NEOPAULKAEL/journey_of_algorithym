/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {
    public TreeNode invertTree(TreeNode root) {
        // 每一个子树的左子树和右子树都对换
        reversal(root);
        return root;
    }

    public void reversal(TreeNode node) {
        // 如果当前为空 返回
        if (node == null) return;
        // 交换左子树 右子树
        TreeNode tmp = node.left;
        node.left = node.right;
        node.right = tmp;
        // 下一层
        if (node.left != null) reversal(node.left);
        if (node.right != null) reversal(node.right);
    }
}
